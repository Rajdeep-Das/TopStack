            <h2>How should I choose a tech training stack?</h2>
            <p>If you are a non-technical founder, it’s tempting to find the nearest developer and rely on their advice when choosing a tech stack. However, developers may be biased towards the technology tools with which they are most comfortable, or judge technologies purely on technical merits rather than business needs. We’ve found the following three step process to be a more objective way of evaluating different technology options. &nbsp; <img class="aligncenter wp-image-1572 size-full" src="https://svsg.co/wp-content/uploads/2015/09/mobile-web-svsg.jpg" alt="mobile-web"></p>
            
                <h3>1. Mobile or Web? Design for your target user base and market</h3>
                <p>More than <strong>60% of web traffic</strong> now comes from mobile devices, and 25% of Americans use only mobile devices to access the internet. These trends will continue to accelerate, so incorporating mobile into your strategy is only a question of when, not if. However, you shouldn’t rely just on general statistics. Refer to the key constituents you identified when designing your MVP and the value proposition your product will offer them. Are users more likely to engage with your product on their phone while on the go, or while sitting at a desk in their office? You should generally launch your MVP on only <strong>one platform</strong> – mobile or web. Spending the additional resources to build and maintain two separate interfaces is better justified after you have made progress in validating your product. With the big picture in mind, the usage patterns of your particular audience will guide you towards a mobile first, mobile only, or mobile later design approach.</p>

                <h4>Mobile first</h4>
                <p>The term “mobile first design” means building a <strong>responsive web app</strong> that adjusts well to all screen sizes, but making sure the UI looks great on a small mobile screen first. Responsive web apps are easy to discover through a google search or a shared link and instantly available without downloading anything. They are cheaper to develop than native apps and easier to iterate on, serving as a useful tool for quickly validating your product. <strong>Launch with a responsive web app if your product:</strong></p>
                <ol>
                  <li>Serves content to a broad audience of desktop and mobile users</li>
                  <li>Contains rich material that can be leveraged for SEO</li>
                  <li>Has a simple UI that can be navigated without frustration on a mobile browser</li>
                  </ol>
                  <h4>Mobile only</h4>
                  <p>In a mobile only approach your MVP will be offered exclusively as a native mobile app downloaded from the app store. Mobile users expect interfaces that look great and work smoothly, and native apps offer the best experience. It is generally advised to develop an iOS app for an MVP and only later add an Android version. Even though Android holds a larger market share with 52% in the US and 80% worldwide, the need to support hundreds of different devices makes it more cumbersome to develop for. There are more than enough iOS users to validate your product and they tend to be more engaged, spending <a href="http://fortune.com/2014/06/27/apples-users-spend-4x-as-much-as-googles/">four times as much</a> as Android users. Launching with both will significantly increase your time to market, and you’ll be doing twice the work on every product iteration. <strong>Launch with a native app if your product:</strong></p>
                    <ol>
                     <li>Will be used primarily on mobile</li>
                     <li>Needs access to device features like the camera, GPS, or accelerometer</li>
                     <li>Will benefit from real-time interactions like push notifications and micro purchases</li>
                     </ol>
