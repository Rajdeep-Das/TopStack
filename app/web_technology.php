           <h3>Front-end tech stack</h3>
           <p>The front-end is the visual part of your application that users will see and interact with. This interaction can happen through a web browser or a mobile app. When building for the web, the front-end tech&nbsp;stack is made up of:</p>
           <ol>
              <li>HTML (Markup Language)</li>
              <li>CSS (Style Sheet Language)</li>
              <li>JavaScript (Scripting Language)</li>
              </ol>
              <p>Front-end frameworks are optional, though recommended:</p>
              <ol>
              <li>JavaScript frameworks include tools for building rich, interactive web experiences. <em>Recommended: AngularJS, Backbone.js, ReactJS</em></li>
              <li>Presentation frameworks provide a standardized format for creating responsive web pages with clean aesthetics. <em>Recommended: Bootstrap</em></li>
              </ol>
              <p>For a mobile app the tech stack is simply comprised of an iOS or Android app written in Objective-C/SWIFT or Java, respectively.</p>



       

