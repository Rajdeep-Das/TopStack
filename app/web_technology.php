            <h2>What is a tech stack?</h2>
            <p>A tech stack is a combination of software products and programming languages used to create a web or mobile application. Applications have two software components: client-side and server-side, also known as front-end and back-end. 
            
             Each layer of the application builds on the features of the one below it, creating a stack. This diagram shows the major building blocks of a typical tech stack, but there can be other supporting&nbsp;components included.</p>
             <img class="aligncenter wp-image-1572 size-full" src="https://svsg.co/wp-content/uploads/2015/09/tech-stack-setup.jpg" alt="tech-stack-setup">
            
             <h3>Back-end tech stack</h3>

             <p>The back-end contains the business logic that works behind the scenes to drive your application. Users will never directly engage with the back-end, all information is passed back and forth through the front-end. The most well known example of a back-end tech stack is the LAMP stack (<strong>L</strong>inux, <strong>A</strong>pache, <strong>M</strong>ySQL, <strong>P</strong>HP). More recent variations of this stack include Ruby or Python as the programming language instead of PHP. A programming language is selected along with a web framework written in that language. Frameworks are incredibly useful because they provide developers with vetted implementations of common web application features like user authentication and data access, saving them from re-inventing the wheel. Popular framework choices include:</p>
